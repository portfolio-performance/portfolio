@SuppressWarnings("nls")
public class FreiburgerKantonalbankPDFExtractorTest
{
    @Test
    public void testWertpapierKauf01()
    {
        var extractor = new FreiburgerKantonalbankPDFExtractor(new Client());

        List<Exception> errors = new ArrayList<>();

        var results = extractor.extract(PDFInputFile.loadTestCase(getClass(), "Kauf01.txt"), errors);

        assertThat(errors, empty());
        assertThat(countSecurities(results), is(1L));
        assertThat(countBuySell(results), is(1L));
        assertThat(countAccountTransactions(results), is(0L));
        assertThat(countAccountTransfers(results), is(0L));
        assertThat(countItemsWithFailureMessage(results), is(0L));
        assertThat(countSkippedItems(results), is(0L));
        assertThat(results.size(), is(2));
        new AssertImportActions().check(results, "CHF");

        // check security
        assertThat(results, hasItem(security( //
                        hasIsin("CH1163584613"), hasWkn("116358461"), hasTicker(null), //
                        hasName("BCF / FKB (CH) Funds - BCF / FKB (CH) Active Dynamic (CHF) -AP- Kapitalisation"), //
                        hasCurrencyCode("CHF"))));

        // check buy sell transaction
        assertThat(results, hasItem(purchase( //
                        hasDate("2023-02-07T00:00:00"), hasShares(100.00), //
                        hasSource("Kauf01.txt"), //
                        hasNote("Auftragsnummer AUF1191526"), //
                        hasAmount("CHF", 10142.00), hasGrossValue("CHF", 10142.00), //
                        hasTaxes("CHF", 0.00), hasFees("CHF", 0.00))));
    }
}
