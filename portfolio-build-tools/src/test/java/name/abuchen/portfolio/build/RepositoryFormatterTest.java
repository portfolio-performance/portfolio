package name.abuchen.portfolio.build;

import static org.junit.Assert.assertEquals;
import static org.junit.Assert.assertTrue;

import java.io.IOException;
import java.nio.charset.StandardCharsets;
import java.nio.file.Files;
import java.nio.file.Path;
import java.util.List;

import org.junit.Test;

public class RepositoryFormatterTest
{
    @Test
    public void writeCanonicalizesManagedFiles() throws IOException
    {
        Path repositoryRoot = Files.createTempDirectory("portfolio-build-tools-test"); //$NON-NLS-1$
        write(repositoryRoot.resolve("example/messages.properties"), //$NON-NLS-1$
                        "#Generated by ResourceBundle Editor (http://essiembre.github.io/eclipse-rbe/)\r\n" //$NON-NLS-1$
                                        + "B = second\r\n" //$NON-NLS-1$
                                        + "A = first\r\n"); //$NON-NLS-1$
        write(repositoryRoot.resolve("example/Messages.java"), //$NON-NLS-1$
                        "package example;\r\n\r\n" //$NON-NLS-1$
                                        + "public class Messages\r\n" //$NON-NLS-1$
                                        + "{\r\n" //$NON-NLS-1$
                                        + "    public static String B;\r\n" //$NON-NLS-1$
                                        + "    public static String A;\r\n" //$NON-NLS-1$
                                        + "}\r\n"); //$NON-NLS-1$

        ManagedBundleSpec spec = new ManagedBundleSpec(Path.of("example"), "messages", Path.of("example/Messages.java")); //$NON-NLS-1$ //$NON-NLS-2$
        RepositoryFormatter formatter = new RepositoryFormatter(repositoryRoot, List.of(spec));

        CheckResult result = formatter.write();

        assertEquals(1, result.rewrittenFiles());
        assertTrue(result.violations().isEmpty());
        assertEquals(
                        "#Generated by ResourceBundle Editor (http://essiembre.github.io/eclipse-rbe/)\n" //$NON-NLS-1$
                                        + "\n" //$NON-NLS-1$
                                        + "A = first\n" //$NON-NLS-1$
                                        + "\n" //$NON-NLS-1$
                                        + "B = second\n", //$NON-NLS-1$
                        Files.readString(repositoryRoot.resolve("example/messages.properties"), StandardCharsets.UTF_8)); //$NON-NLS-1$
        assertEquals(
                        "package example;\r\n\r\n" //$NON-NLS-1$
                                        + "public class Messages\r\n" //$NON-NLS-1$
                                        + "{\r\n" //$NON-NLS-1$
                                        + "    public static String B;\r\n" //$NON-NLS-1$
                                        + "    public static String A;\r\n" //$NON-NLS-1$
                                        + "}\r\n", //$NON-NLS-1$
                        Files.readString(repositoryRoot.resolve("example/Messages.java"), StandardCharsets.UTF_8)); //$NON-NLS-1$
    }

    @Test
    public void checkReportsDetailedViolations() throws IOException
    {
        Path repositoryRoot = Files.createTempDirectory("portfolio-build-tools-test"); //$NON-NLS-1$
        write(repositoryRoot.resolve("example/messages.properties"), //$NON-NLS-1$
                        "#Generated by ResourceBundle Editor (http://essiembre.github.io/eclipse-rbe/)\r\n" //$NON-NLS-1$
                                        + "B = second\r\n" //$NON-NLS-1$
                                        + "A = first\r\n"); //$NON-NLS-1$
        write(repositoryRoot.resolve("example/Messages.java"), //$NON-NLS-1$
                        "package example;\r\n\r\n" //$NON-NLS-1$
                                        + "public class Messages\r\n" //$NON-NLS-1$
                                        + "{\r\n" //$NON-NLS-1$
                                        + "    public static String B;\r\n" //$NON-NLS-1$
                                        + "    public static String A;\r\n" //$NON-NLS-1$
                                        + "}\r\n"); //$NON-NLS-1$

        ManagedBundleSpec spec = new ManagedBundleSpec(Path.of("example"), "messages", Path.of("example/Messages.java")); //$NON-NLS-1$ //$NON-NLS-2$
        RepositoryFormatter formatter = new RepositoryFormatter(repositoryRoot, List.of(spec));

        CheckResult result = formatter.check();
        List<String> messages = result.violations().stream().map(Violation::message).toList();

        assertTrue(result.hasViolations());
        assertTrue(messages.contains("wrong line endings; expected LF")); //$NON-NLS-1$
        assertTrue(messages.contains("property keys are not sorted")); //$NON-NLS-1$
        assertTrue(!messages.contains("Messages.java declarations are not sorted")); //$NON-NLS-1$
    }

    private void write(Path file, String content) throws IOException
    {
        Files.createDirectories(file.getParent());
        Files.writeString(file, content, StandardCharsets.UTF_8);
    }
}
